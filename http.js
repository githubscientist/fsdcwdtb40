/*
    HTTP Requests:

    To make api calls or http requests, we can use the following methods:

    1. XHR: XML HTTP Request
        - Old way of making http requests, not recommended for modern applications.
        - Requires more code and is less efficient than modern methods.

    2. Fetch API
        - Modern way of making http requests, built into modern browsers.
        - Less code compared to XHR, and more efficient.
        - It is built on Promises, making it easier to handle asynchronous operations.

    3. Axios
        - A popular third-party library for making http requests.
        - Provides a simple and clean API for making http requests.
        - It is built on Promises and works in both browser and Node.js environments.
*/

